package pojo;

public class GiaoVien {
	private int maGV;
	private String tenGV;

	public GiaoVien() {
	}

	public GiaoVien(int maGV, String tenGV) {
		this.maGV = maGV;
		this.tenGV = tenGV;
	}

	public int getMaGV() {
		return maGV;
	}

	public void setMaGV(int maGV) {
		this.maGV = maGV;
	}

	public String getTenGV() {
		return tenGV;
	}

	public void setTenGV(String tenGV) {
		this.tenGV = tenGV;
	}
}
